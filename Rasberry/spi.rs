// Common byte sending function
fn send_bytes(spi: &mut Spi, bytes_to_send: &[u8], mode_name: &str) -> Result<()> {
    if bytes_to_send.is_empty() {
        println!("⚠️  {} mode byte data is empty, please fill in data first", mode_name);
        return Ok(());
    }
    
    println!("Starting to send {} bytes...\n", bytes_to_send.len());
    
    // Send bytes one by one
    for (i, &byte) in bytes_to_send.iter().enumerate() {
        println!("Byte {}: 0x{:02X}", i + 1, byte);
        
        // Send single byte
        match spi.write(&[byte]) {
            Ok(_) => println!("  ✓ Sent successfully"),
            Err(e) => println!("  ✗ Send failed: {}", e),
        }
        
        // Wait 10ms before sending next byte
        if i < bytes_to_send.len() - 1 {
            thread::sleep(Duration::from_millis(10));
        }
    }
    
    println!("All bytes sent successfully!");
    Ok(())
}
