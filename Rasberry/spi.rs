use rppal::spi::{Bus, Mode, SlaveSelect, Spi};
use anyhow::Result;
use std::thread;
use std::time::Duration;
use std::io::{self, Write};

fn main() -> Result<()> {
    println!("=== FPGA LED Control Program ===");
    
    // Initialize SPI
    let mut spi = Spi::new(Bus::Spi0, SlaveSelect::Ss0, 1_000_000, Mode::Mode0)?;
    println!("✓ SPI initialized successfully (1MHz, Mode0)\n");
    
    loop {
        // Display menu
        println!("Please select LED mode:");
        println!("1. Single LED");
        println!("2. Row LED");
        println!("3. Water LED");
        println!("4. PWM LED");
        println!("5. Picture LED");
        println!("6. Animation LED");
        println!("0. Exit program");
        print!("Enter your choice (0-6): ");
        io::stdout().flush()?;
        
        // Read user input
        let mut input = String::new();
        io::stdin().read_line(&mut input)?;
        let choice = input.trim();
        
        match choice {
            "1" => {
                println!("\n--- Single LED Mode ---");
                send_single_led_mode(&mut spi)?;
            },
            "2" => {
                println!("\n--- Row LED Mode ---");
                send_row_led_mode(&mut spi)?;
            },
            "3" => {
                println!("\n--- Water LED Mode ---");
                send_water_led_mode(&mut spi)?;
            },
            "4" => {
                println!("\n--- PWM LED Mode ---");
                send_pwm_led_mode(&mut spi)?;
            },
            "5" => {
                println!("\n--- Picture LED Mode ---");
                send_picture_led_mode(&mut spi)?;
            },
            "6" => {
                println!("\n--- Animation LED Mode ---");
                send_animation_led_mode(&mut spi)?;
            },
            "0" => {
                println!("Exiting program");
                break;
            },
            _ => {
                println!("Invalid choice, please try again\n");
                continue;
            }
        }
        
        println!("Mode execution completed!\n");
    }
    
    Ok(())
}

// Single LED Mode
fn send_single_led_mode(spi: &mut Spi) -> Result<()> {
    println!("Executing Single LED Mode...");
    
    let bytes_to_send = [

        0x01,0x02,0x04,
    ];
    
    send_bytes(spi, &bytes_to_send, "Single LED")?;
    Ok(())
}


